def calcular_transicion(df_est):
    counts = np.zeros((N_EST, N_EST), dtype=float)
    for _, grp in df_est.sort_values('periodo').groupby('municipio'):
        seq = grp['estado'].values
        for t in range(len(seq) - 1):
            s0, s1 = int(seq[t]), int(seq[t+1])
            if 0 <= s0 < N_EST and 0 <= s1 < N_EST:
                counts[s0, s1] += 1
    row_s = counts.sum(axis=1, keepdims=True)
    return np.where(row_s > 0, counts / row_s, 1.0/N_EST), counts

