"""---
## 9. Modelo Bayesiano — Combinación de Evidencias

Prior: tasa base histórica de desastres.
Evidencias secuenciales vía likelihood ratios:
1. Probabilidad del clasificador
2. Rareza del municipio (distancia al centroide)
3. Anomalía en la precipitación predicha
"""

