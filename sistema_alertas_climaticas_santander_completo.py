def comparar_meses_desastre_vs_normal(df_clima, df_eventos, nombre_variable):
    """Compara promedios mensuales en meses con/sin desastre."""
    if len(df_clima) == 0:
        return

    # Promedios mensuales departamentales
    mensual = df_clima.groupby(
        df_clima['fechaobservacion'].dt.to_period('M')
    )['valorobservado'].mean().reset_index()
    mensual.columns = ['periodo', 'valor']

    # Meses con al menos un desastre
    meses_desastre = set(df_eventos['fecha_de_ocurrencia'].dt.to_period('M').dropna())
    mensual['tiene_desastre'] = mensual['periodo'].isin(meses_desastre)

    n_con = mensual['tiene_desastre'].sum()
    n_sin = (~mensual['tiene_desastre']).sum()

    if n_con > 0 and n_sin > 0:
        media_con = mensual.loc[mensual['tiene_desastre'], 'valor'].mean()
        media_sin = mensual.loc[~mensual['tiene_desastre'], 'valor'].mean()
        std_sin = mensual.loc[~mensual['tiene_desastre'], 'valor'].std()

        print(f"\n{nombre_variable}:")
        print(f"  Promedio en meses CON desastre:  {media_con:.2f}  (n={n_con} meses)")
        print(f"  Promedio en meses SIN desastre:  {media_sin:.2f}  (n={n_sin} meses)")
        print(f"  Diferencia:                      {media_con - media_sin:+.2f}")
        if std_sin > 0:
            z = (media_con - media_sin) / std_sin
            print(f"  Diferencia en desviaciones std:  {z:+.2f}")

        fig, ax = plt.subplots(figsize=(10, 5))
        mensual.loc[~mensual['tiene_desastre'], 'valor'].hist(
            bins=20, ax=ax, alpha=0.6, color='#3498db',
            label=f'Sin desastre (n={n_sin})', density=True)
        mensual.loc[mensual['tiene_desastre'], 'valor'].hist(
            bins=20, ax=ax, alpha=0.6, color='#e74c3c',
            label=f'Con desastre (n={n_con})', density=True)
        ax.axvline(media_sin, color='#3498db', linestyle='--', linewidth=2)
        ax.axvline(media_con, color='#e74c3c', linestyle='--', linewidth=2)
        ax.set_title(f'{nombre_variable}: meses con desastre vs sin desastre', fontweight='bold')
        ax.set_xlabel(nombre_variable)
        ax.set_ylabel('Densidad')
        ax.legend()
        plt.tight_layout()
        plt.show()
    else:
        print(f"\n{nombre_variable}: datos insuficientes")

