# Funcion para hacer el merge multi-nivel de una variable
def merge_clima_multinivel(df_cruce, muni_data, dept_data, nombre):
    """Merge con datos municipales (nivel 1 y 2) + fallback departamental (nivel 3)."""
    if len(muni_data) == 0 and len(dept_data) == 0:
        print(f"  {nombre}: sin datos disponibles")
        return df_cruce

    cols_valor = [f'{nombre}_media', f'{nombre}_std', f'{nombre}_min', f'{nombre}_max']

    # Merge nivel 1+2: datos por municipio (usando muni_clima del mapeo)
    if len(muni_data) > 0:
        df_cruce = df_cruce.merge(
            muni_data[['periodo', 'municipio'] + cols_valor + [f'{nombre}_n']],
            left_on=['periodo', 'muni_clima'],
            right_on=['periodo', 'municipio'],
            how='left',
            suffixes=('', f'_clima_{nombre}')
        )
        # Eliminar columna municipio duplicada del merge
        col_dup = f'municipio_clima_{nombre}'
        if col_dup in df_cruce.columns:
            df_cruce = df_cruce.drop(columns=[col_dup])

    # Merge nivel 3: datos departamentales
    if len(dept_data) > 0:
        df_cruce = df_cruce.merge(dept_data, on='periodo', how='left')

        # Rellenar nulos con promedios departamentales
        for col in cols_valor:
            col_dept = f'{col}_dept'
            if col in df_cruce.columns and col_dept in df_cruce.columns:
                df_cruce[col] = df_cruce[col].fillna(df_cruce[col_dept])
            elif col not in df_cruce.columns and col_dept in df_cruce.columns:
                df_cruce[col] = df_cruce[col_dept]

        # Eliminar columnas _dept
        dept_cols = [c for c in df_cruce.columns if c.endswith('_dept')]
        df_cruce = df_cruce.drop(columns=dept_cols)

    n_valid = df_cruce[f'{nombre}_media'].notna().sum() if f'{nombre}_media' in df_cruce.columns else 0
    print(f"  {nombre}: {n_valid} de {len(df_cruce)} eventos con datos ({n_valid/len(df_cruce)*100:.1f}%)")
    return df_cruce

