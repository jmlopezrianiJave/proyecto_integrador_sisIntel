def detectar_anomalias_por_municipio(df, nombre_variable):
    if len(df) == 0 or 'municipio' not in df.columns or 'valorobservado' not in df.columns:
        print(f"  {nombre_variable}: sin datos o columnas")
        return df

    for col in ['media_muni', 'std_muni', 'z_score', 'es_anomalia']:
        if col in df.columns:
            df = df.drop(columns=[col])

    df = df.copy()
    df['media_muni'] = df.groupby('municipio')['valorobservado'].transform('mean')
    df['std_muni'] = df.groupby('municipio')['valorobservado'].transform('std')
    df['z_score'] = np.where(df['std_muni'] > 0,
        (df['valorobservado'] - df['media_muni']) / df['std_muni'], 0)
    df['es_anomalia'] = df['z_score'].abs() > 3

    n_anomalias = df['es_anomalia'].sum()
    pct = n_anomalias / len(df) * 100
    print(f"  {nombre_variable}: {n_anomalias:,} anomalias ({pct:.2f}%) de {len(df):,} registros")
    return df

